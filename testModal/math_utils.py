# math_utils.py
"""数学工具模块"""

PI = 3.1415926

def circle_area(radius):
    """计算面积"""
    return PI * radius**2

def factorial(n):
    """计算阶乘"""
    if n==0:
        return 1
    return n * factorial(n - 1)

if __name__ == "__main__":
    # 模块测试代码
    print(f"圆面积: {circle_area(5)}")
    print(f"5的阶乘: {factorial(5)}")

